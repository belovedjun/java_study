public class OperatorMain02 {
	public static void main(String[] args) {
		System.out.println("===증감연산자===");
		
		int i = 5;
		
		System.out.println(i++);//5
		System.out.println(i);
		
		System.out.println("-----------");
		
		int j = 10;
		
		System.out.println(j--);//10
		System.out.println(j);
		
	}
}
