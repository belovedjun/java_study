public class OperatorMain01 {
	public static void main(String[] args) {
		System.out.println("===증감연산자===");
		
		int i = 5;
		//증가연산자 (증가연산자는 계속 값이 증가함)
		System.out.println(++i);//6
		
		System.out.println("----------");
		
		int j = 10;
		//감소연산자 (감소연산자는 계속 값이 감소함)
		System.out.println(--j);//9
		
	}
}
