public class OpreatorMain03 {
	public static void main(String[] args) {
		System.out.println("===산술연산자===");
		
		System.out.println(1 + 1); //+더하기
		System.out.println(10 - 3);//-빼기
		System.out.println(10 * 3);//*곱하기
		System.out.println(10 / 3);//몫
		System.out.println(10.0 / 3);//10.0 / 3.0(int -> double) 자동형변환
		System.out.println(5 % 3);//나머지
	}
}
